#定义函数必须写在被调用函数之前

#定义函数printMenu()
def printMenu():
    print("="*50)
    print("名片管理系统")
    print("该函数已经被调用")
    print("="*50)

#定义函数printSanJiaoXing()
def printSanJiaoXing():
    print("*")
    print("*"*2)
    print("*"*3)
    print("*"*4)
    print("*"*5)
    
#调用函数
printMenu()
